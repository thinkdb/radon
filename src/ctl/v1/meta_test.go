func TestCtlV1Versionz(t *testing.T) {
	log := xlog.NewStdLog(xlog.Level(xlog.PANIC))
	fakeDbs, proxyNew, cleanup := proxy.MockProxy(log)
	defer cleanup()
	address := proxyNew.Address()

	// fakeDbs.
	{
		fakeDbs.AddQueryPattern("create table .*", &sqltypes.Result{})
	}

	// create test table.
	{
		client, err := driver.NewConn("mock", "mock", address, "", "utf8")
		assert.Nil(t, err)
		query := "create table test.t1(id int, b int) partition by hash(id)"
		_, err = client.FetchAll(query, -1)
		assert.Nil(t, err)
	}

	{
		api := rest.NewApi()
		router, _ := rest.MakeRouter(
			rest.Get("/v1/meta/versions", VersionzHandler(log, proxyNew)),
		)
		api.SetApp(router)
		handler := api.MakeHandler()

		recorded := test.RunRequest(t, handler, test.MakeSimpleRequest("GET", "http://localhost/v1/meta/versions", nil))
		recorded.CodeIs(200)

		got := strings.Contains(recorded.Recorder.Body.String(), "version")
		assert.True(t, got)
	}
}

